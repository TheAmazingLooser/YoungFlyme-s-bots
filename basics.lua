function CDOTA_Bot_Script:NeedMana()
	local MaxMana = 0
	for i = 0,23 do
		local ability = self:GetAbilityInSlot(i)
		if ability ~= nil then
			if self:GetMana() < ability:GetManaCost() and ability:GetManaCost() < self:GetMaxMana() then 
				return true
			end
		end
	end
	return false
end

