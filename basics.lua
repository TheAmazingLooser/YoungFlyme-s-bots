function CDOTA_Bot_Script:GetForwardVector()
	local radians = self:GetFacing() * math.pi / 180
	local forward_vector = Vector(math.cos(radians), math.sin(radians))
	return forward_vector
end

function CDOTA_Bot_Script:IsStuck()
	if self.stuckLoc ~= nil and self.stuckTime ~= nil then 
		local attackTarget = self:GetAttackTarget()
		local EAd = GetUnitToUnitDistance(self, GetAncient(GetOpposingTeam()))
		local TAd = GetUnitToUnitDistance(self, GetAncient(GetTeam()))
		local Et = self:GetNearbyTowers(450, true)
		local At = self:GetNearbyTowers(450, false)
		if self:GetCurrentActionType() == BOT_ACTION_TYPE_MOVE_TO and attackTarget == nil and EAd > 2200 and TAd > 2200 and #Et == 0 and #At == 0 and DotaTime() > self.stuckTime + 5.0 and GetUnitToLocationDistance(self, self.stuckLoc) < 25 then
			print(self:GetUnitName().." is stuck")
			return true
		end
	end
	return false
end

function CDOTA_Bot_Script:GetXUnitsInFront( nUnits )
	return self:GetLocation() + self:GetForwardVector() * nUnits
end

