function IsTowerAlive(Team,Tower)
	if type(Tower) == "table" then
		for k,v in pairs(Tower) do
			if GetTower(Team,v) ~= nil and GetTower(Team,v):IsAlive() then
				return true
			else
				return false
			end
		end
	else
		if GetTower(Team,Tower) ~= nil and GetTower(Team,Tower):IsAlive() then
			return true
		else
			return false
		end
	end
end

function IsNextTowerAlive(Team,Tower)
	if type(Tower) == "table" then
		for k,v in pairs(Tower) do
			local tow = GetTowerNext(v)
			if GetTower(Team,v) ~= nil and GetTower(Team,v):IsAlive() then
				return true
			else
				return false
			end
		end
	else
		local tow = GetTowerNext(Tower)
		if GetTower(Team,Tower) ~= nil and GetTower(Team,Tower):IsAlive() then
			return true
		else
			return false
		end
	end
end

