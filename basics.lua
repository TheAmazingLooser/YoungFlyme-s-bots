function Ward(Vec) -- For getting IsWarded
	for k,v in pairs(Wards) do
		--print("k,v = ",tostring(k),tostring(v),v.Pos.x,Vec.x)
		if v.Pos.x == Vec.x then
			for kK,vV in pairs(v.Tower) do
				table.insert(Warded[vV],{Time = GameTime(), Team = v.Team})
				return true
			end
		end
	end
	return false
