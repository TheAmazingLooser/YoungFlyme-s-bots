			if v.is_attack then
				PhysicDamage = PhysicDamage + self:GetActualIncomingDamage(v.caster:GetAttackDamage() + v.caster:GetBaseDamageVariance(), DAMAGE_TYPE_PHYSICAL)
			else
				local ability = v.ability
				local dmg = ability:GetAbilityDamage()
				local damageType = ability:GetDamageType()
				if damageType == DAMAGE_TYPE_PHYSICAL then
					PhysicDamage = PhysicDamage + self:GetActualIncomingDamage(dmg, damageType)
				elseif damageType == DAMAGE_TYPE_MAGICAL then
					MagicDamage = MagicDamage + self:GetActualIncomingDamage(dmg, damageType)
				elseif damageType == DAMAGE_TYPE_PURE then
					PureDamage = PureDamage + self:GetActualIncomingDamage(dmg, damageType)
				end
			end
		end
		local dmg = {
			["Physic"] = PhysicDamage,
			["Magic"] = MagicDamage,
			["Pure"] = PureDamage,
			["All"] = PhysicDamage+MagicDamage+PureDamage,
		}
		return dmg
	end
end

