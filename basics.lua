	for k,v in pairs(Wards) do
		--print("k,v = ",tostring(k),tostring(v),v.Pos.x,Vec.x)
		if v.Pos.x == Pos.x then
			return IsWarded(v.Tower,v.Team)
		end
	end
end

function IsWarded(TowerTable,Team)
	for _,Tower in pairs(TowerTable) do
		for k,v in pairs(Warded) do
			if k == Tower then
				for _,Value in pairs(v) do
					if Value.Team == Team then
						return true
					end
				end
			end
		end
	end
	return false
end

